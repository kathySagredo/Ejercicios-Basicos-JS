let NumeroX = parseInt(prompt("Verificar cual número es mayor. Escriba número X:"));
let NumeroY = parseInt(prompt("Verificar cual número es mayor. Escriba número Y:"));

if (NumeroX > NumeroY) {
    document.write("El número mayor es: ", NumeroX)
} else {
    document.write("El número mayor es: ", NumeroY)
}
